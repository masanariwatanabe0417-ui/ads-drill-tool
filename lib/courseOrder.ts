// コース名 → そのシリーズ内での 1 始まりの番号。見つからなければ null。
// 表記ゆれに少しだけ強くするため、前後空白を無視して照合する。
export function courseNumber(series: string, course: string): number | null {
  const list = COURSE_ORDER[series?.trim()];
  if (!list) return null;
  const target = course?.trim();
  const i = list.findIndex((c) => c.trim() === target);
  return i >= 0 ? i + 1 : null;
}
